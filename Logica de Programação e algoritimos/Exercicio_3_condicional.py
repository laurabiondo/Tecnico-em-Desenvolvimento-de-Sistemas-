diaDaSemana = int(input("digite um número entre 1 e 7: "))

if (diaDaSemana == 1):
    print ("DOMINGO")

elif (diaDaSemana == 2):
    print("SEGUNDA-FEIRA")

elif (diaDaSemana == 3):
    print ("TERÇA-FEIRA")

elif (diaDaSemana == 4):
    print ("QUARTA-FEIRA")

elif (diaDaSemana == 5):
    print ("QUINTA-FEIRA")

elif (diaDaSemana == 6):
    print ("SEXTA- FEIRA")

elif (diaDaSemana == 7):
    print ("SÁBADO")

else:
    print ("ERRO")